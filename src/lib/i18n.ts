export type Lang = 'sl' | 'en' | 'de' | 'it';

export const LANGS: Lang[] = ['sl', 'en', 'de', 'it'];

export function normaliseLang(raw: string | undefined | null): Lang {
  if (!raw) return 'en';
  const s = String(raw).trim().toLowerCase();
  if (!s) return 'en';
  // ISO-ish prefix: sl-SI → sl, en-US → en
  const prefix = s.split(/[-_]/)[0];
  if (prefix === 'sl' || s.startsWith('sloven')) return 'sl';
  if (prefix === 'en' || s.startsWith('english')) return 'en';
  if (prefix === 'de' || s.startsWith('german') || s.startsWith('deutsch')) return 'de';
  if (prefix === 'it' || s.startsWith('ital')) return 'it';
  return 'en';
}

